public class TestSwitch2 {
    public static void main(String[] args) {
        char ch = 'a';
        switch (ch) {
            case 'a':
                System.out.println("王者");
                break;
            case 'b':
                System.out.println("黄金");
                break;
            default:
                System.out.println("白银");
        }
    }
}
